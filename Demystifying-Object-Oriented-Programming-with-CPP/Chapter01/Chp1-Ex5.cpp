// Purpose: Review simple looping with for loops

#include <iostream>

using namespace std;

int main()
{
    int i;
    
    for (i = 0; i < 10; i++)
        cout << i << endl;

    for (int j = 0; j < 10; j++)
        cout << j << endl;

	
    return 0;
}
